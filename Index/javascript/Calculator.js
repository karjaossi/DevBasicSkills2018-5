// This small piece of JavaScript will take user input and
// convert it to Hex,Dec,Bin and Oct values.

function blockinputB ()
{
  var B = document.getElementById('inputB');
  document.addEventListener("keypress", function (B){
  if (B.keyCode != 48 && B.keyCode != 49 && B.keyCode != 8)
  {
    return;
  }
  else {
    {
      return false;
    }
  }
})}
